package Collections;

public class ListDemo2 {
	@Override
	public String toString() {
		return "ListDemo2 [c=" + c + ", d=" + d + "]";
	}
	private int c;
	private int d;
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	

}
